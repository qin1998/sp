	var winHref  = document.location.href ,
		Href     = winHref.split("#")[1];
	Initialize(Href)
	$(".footer .list li").eq(0).find("a").click(function(e){
		setTimeout(function(){
			var winHref  = document.location.href ,
				Href     = winHref.split("#")[1];
			Initialize(Href)
		}, 100)
	})
})
function Initialize(e){
	switch(e){
		case ('About') : $("html,body").stop().animate({scrollTop : [$(".brand-about").offset().top, 'easeInExpo']}, 1000)
		break;
		case ('History') : $("html,body").stop().animate({scrollTop : [$(".brand-history").offset().top, 'easeInExpo']}, 1000)
		break;
	};
};
